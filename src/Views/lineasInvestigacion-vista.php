                                    productivo y social; mejorando la competitividad, y el bienestar, disminuyendo el impacto ambiental del uso de energéticos con 
                                    respecto a los medios más tradicionales. De manera indirecta también se contribuye con la estrategia de Seguridad, y esto es 
                                    consecuencia de la disminución en la demanda de energéticos tradicionales, en las cuales normalmente hay una dependencia del 
