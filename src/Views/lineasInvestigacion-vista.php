                                <div class="col-12" style="text-align: justify;">
                                    <p style="text-align: justify;">
                                    Desarrollar sistemas de control que permitan: disminuir los consumos de energía de equipos e incrementar 
                                    la eficiencia energética de los procesos y otros aspectos del sector productivo y de servicios.<br /><br />

                                    El desarrollo tecnológico acelerado, unido al gran poder de procesamiento y velocidad que han alcanzado 
                                    las computadoras, hace que hoy día sea prácticamente imposible concebir una actividad en cualquier proceso 
                                    industrializado que no esté asociada a un equipo de control automático, y los procesos relacionados con 
                                    el tema energético no son la excepción. Los sistemas de control de procesos son tan frecuentes y de tal 
                                    magnitud, que obligan a estar permanentemente actualizado en Consciente de esta necesidad el Instituto 
                                    Tecnológico de Veracruz ofrece la línea de Control de Procesos Energéticos, cuyo principal objetivo es 
                                    capacitar a estudiantes y profesionales con los procedimientos, herramientas de software y hardware 
                                    requeridas para el desarrollo del control de los procesos llevados en el sector energético.<br /><br />

                                    Esta línea de generación y aplicación del conocimiento le permitirá al alumno desarrollar una temática y 
                                    un trabajo, que posteriormente le servirá como soporte a los programas de posgrado; definiendo nuevas rutas 
                                    de investigación para diversos grupos y fortaleciendo los mismos con el desarrollo de proyectos de calidad. 
                                    Además, el alumno contará con conocimientos que lo impulsarán en otras temáticas del área de la automatización 
                                    industrial; generando que sean capaces de aplicar nuevos conocimientos e ideas, que conlleven al diseño y 
                                    desarrollo de proyectos de reducido impacto ambiental, eficientes y dentro de las reglamentaciones de seguridad 
                                    correspondientes.
                                    </p>
