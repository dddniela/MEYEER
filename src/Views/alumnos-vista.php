        <h2 class="seccion-texto-titulo">Objetivo General</h2>
        <div class="seccion-texto-separador"></div>
        <p class="seccion-texto-parrafo">Generar profesionistas de alta calidad, que respondan a los retos de la sociedad en el uso eficiente del recurso energético, con capacidad para fortalecer la relación entre el sector productivo y las nuevas tecnologías de control y energía eficiente, siempre teniendo en cuenta la responsabilidad social y ambiental a nivel nacional e internacional. </p>

