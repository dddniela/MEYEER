        <li class="seccion-texto-lista-item">Identificar las áreas de oportunidad en el ámbito energético para disminuir los consumos de energía en la fabricación, manufactura de un producto, requerimientos. Analizar la factibilidad para incrementar la eficiencia energética de los procesos y otros aspectos del sector productivo y de servicios.</li>
        <li class="seccion-texto-lista-item">Establecer las técnicas y estrategias energéticas para suministrar y generar potencia a los sistemas auxiliares de proceso con referente a las fuentes alternas de energías renovables.</li>
