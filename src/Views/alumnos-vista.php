<!-- Sección: Proceso de admisión -->
<section class="seccion-contenedor modoOscuro">

    <div class="seccion-texto">
        <h2 class="seccion-texto-titulo">Requisitos para la permanencia en el programa</h2>
        <div class="seccion-texto-separador"></div>
        <li class="seccion-texto-lista-item">Los estudiantes de la maestría disponen de 24 meses, contados a partir de la fecha en que iniciaron los estudios, para cubrir todos los créditos del plan de estudios y aprobar el examen de grado correspondiente.</li>
        <li class="seccion-texto-lista-item">Inscribirse en el periodo correspondiente durante todo el programa de maestría hasta la obtención del grado. Después de los períodos reglamentarios, el alumno deberá inscribirse aunque no tenga carga académica.</li>
        <li class="seccion-texto-lista-item">Aprobar con una calificación mínima de 70 en todas sus asignaturas y mantener un promedio general mínimo de 80.</li>
        <li class="seccion-texto-lista-item">No reprobar en dos ocasiones una misma asignatura o reprobar dos asignaturas diferentes.</li>
        <li class="seccion-texto-lista-item">No abandonar sus estudios por un periodo mayor de seis meses sin previa autorización del Jefe de la División de Estudios de Posgrado e Investigación. Las bajas temporales y definitivas serán solicitadas al Coordinador y valoradas por el Consejo de Posgrado.</li>
        <li class="seccion-texto-lista-item">Entregar a la coordinación de posgrado el informe de avance de su proyecto de tesis al término de cada periodo escolar, el cual deberá estar avalado por el director de tesis.</li>
        <li class="seccion-texto-lista-item">Obtener el grado en un período no mayor de 4 años contados a partir de la fecha de ingreso al programa.</li>
        <li class="seccion-texto-lista-item">Acatar las disposiciones reglamentarias para el buen funcionamiento de la institución, y no ser sujeto de sanciones por parte de las autoridades competentes.</li>
        <li class="seccion-texto-lista-item">Incumplir cualquiera de estas u otras disposiciones emitidas por las instancias educativas al efecto será causa para la baja definitiva del estudiante del programa respectivo.</li>
        
    </div>
